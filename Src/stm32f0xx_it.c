/**
  * @brief This function handles USART1 global interrupt.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */
uint32_t temp;
	if(__HAL_UART_GET_FLAG(&huart1,UART_FLAG_IDLE)==SET)//1,判断串口1是否为空闲中断
	{
		__HAL_UART_CLEAR_IDLEFLAG(&huart1);               //2,清除中断
		HAL_UART_DMAStop(&huart1);                        //3,停止DMA接收
		
		                                                  //只要读一次就能清除DMA的中断
		temp = huart1.Instance->ISR;
		temp = huart1.Instance->TDR;
		temp = USART1_DMA_REC_SIZE-hdma_usart1_rx.Instance->CNDTR;//获取DMA接收数据的长度
		Usart1type.Usart1DMARecLen = temp;
		HAL_UART_RxCpltCallback(&huart1);                 //调取串口1的回调函数
	}
	
  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */
  HAL_UART_Receive_DMA(&huart1,Usart1type.Usart1DMARecBuff,USART1_DMA_REC_SIZE);//6，开启DMA接收
  /* USER CODE END USART1_IRQn 1 */
}

